DROP TABLE IF EXISTS user;
DROP TABLE IF EXISTS foodcategory;
DROP TABLE IF EXISTS fooditem;
DROP TABLE IF EXISTS transactionlog;
DROP TABLE IF EXISTS menu;
DROP TABLE IF EXISTS processeditem;


CREATE TABLE IF NOT EXISTS user (
  userid INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
  username TEXT NOT NULL,
  hashpw TEXT NOT NULL,
  saltpw TEXT NOT NULL,
  avatar TEXT,
	authToken varchar NOT NULL,
  is_admin boolean  NOT NULL,
  restaurantname TEXT,
  address TEXT,
  location TEXT NOT NULL,
  contact TEXT
);

CREATE TABLE IF NOT EXISTS foodcategory (
  categoryid INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
  categoryname TEXT NOT NULL,
  description TEXT
);

CREATE TABLE IF NOT EXISTS fooditem (
  itemid INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
  userid INTEGER NOT NULL, 
  foodCategoryid INTEGER NOT NULL, 
  name TEXT NOT NULL,
  quantity REAL NOT NULL,
  unit TEXT NOT NULL,
  timestamp TEXT NOT NULL, 
  batchnumber TEXT NOT NULL,
  expirydate TEXT NOT NULL, 
  pricePerUnit REAL NOT NULL,
  readstatus TEXT NULL,
  FOREIGN KEY (userid) REFERENCES user (userid) ON DELETE CASCADE,
  FOREIGN KEY (foodCategoryid) REFERENCES foodcategory (categoryid) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS menu(
	dishid	INTEGER NOT NULL,
  userid	INTEGER NOT NULL,
  dishname TEXT NOT NULL,
  dishpic TEXT NOT NULL,
  ingredientname TEXT NOT NULL,
	weight	INTEGER NOT NULL,
	preservationtime	TEXT NOT NULL,
  FOREIGN KEY (userid) REFERENCES user (userid)
);



CREATE TABLE IF NOT EXISTS transactionlog (
  transctionid INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
  userid INTEGER NOT NULL, 
  foodItemid INTEGER NOT NULL, 
  quantity REAL NOT NULL,
  unit TEXT NOT NULL, -- e.g., 'kg'
  priceperunit REAL NOT NULL,
  timestamp TEXT NOT NULL, 
  act TEXT NOT NULL, 
  newexpirydate TEXT, 
  FOREIGN KEY (userid) REFERENCES user (userid),
  FOREIGN KEY (fooditemid) REFERENCES fooditem (itemid)
);

CREATE TABLE IF NOT EXISTS categoryassociation (
  categoryid INTEGER ,
	categoryname	TEXT NOT NULL,
	itemname	TEXT NOT NULL,
  FOREIGN KEY (categoryid) REFERENCES foodcategory (categoryid)
);


